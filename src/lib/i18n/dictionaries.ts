/** Tiny template interpolation: t('{n} left', { n: 5 }) -> '5 left' */
export function t(template: string, vars: Record<string, string | number>): string {
  return template.replace(/\{(\w+)\}/g, (_, key) => String(vars[key] ?? ''));
}

